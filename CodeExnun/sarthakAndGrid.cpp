#include <bits/stdc++.h>
using namespace std;
typedef long long int lli;
int main()
{
    lli t;
    cin>>t;

    while(t--)
    {
        lli n;
        cin>>n;

        for(int i=0;i<n;i++)
        {
            for(int j=0;j<n;j++)
            {
                cout<<"1 ";
            }

            printf("\n");
        }
    }
}